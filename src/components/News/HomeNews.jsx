import { Link, NavLink } from 'react-router-dom'
import line from '../../assets/decor-right-black.svg'
import { FaArrowAltCircleRight, FaArrowLeft, FaArrowRight } from 'react-icons/fa'
import { Swiper, SwiperSlide } from 'swiper/react'
import { Navigation, Autoplay } from 'swiper/modules'
import 'swiper/css'
import 'swiper/css/navigation'
import { useEffect, useState } from 'react'
import { useTranslation } from 'react-i18next'
import Skeleton from 'react-loading-skeleton'
import 'react-loading-skeleton/dist/skeleton.css'

function HomeNews() {
  const baseUrl = import.meta.env.VITE_API_BASE_URL
  const uploadBase = import.meta.env.VITE_API_UPLOAD_BASE
  const { i18n, t } = useTranslation()
  const lang = ['uz', 'ru'].includes(i18n.language) ? i18n.language : 'uz'

  const [data, setData] = useState([])
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    setLoading(true)
    fetch(`${baseUrl}/api/news`)
      .then((res) => res.json())
      .then((resData) => {
        if (Array.isArray(resData?.data)) {
          setData(resData.data)
        } else {
          setData([])
        }
      })
      .catch((err) => {
        console.error('Xatolik:', err)
        setData([])
      })
      .finally(() => {
        setLoading(false)
      })
  }, [])

  return (
    <div className="bg-gray-100">
      <section className="mx-auto max-w-7xl mt-15 flex flex-col items-start py-10 px-[1rem]">
        {/* Sarlavha */}
        <h3 className="text-xl md:text-2xl text-black/90 lg:text-4xl flex font-bold items-center font-one gap-3 ">
          {loading ? (
            <Skeleton width={300} height={36} />
          ) : (
            <>
              <span>{t('news.homeTitle')}</span>
              <img
                src={line}
                alt="Line Decoration"
                className="hidden sm:block sm:max-w-40 md:max-w-70"
              />
            </>
          )}
        </h3>

        {/* Swiper yoki Skeleton */}
        <div className="relative w-full">
          <Swiper
            modules={[Navigation, Autoplay]}
            spaceBetween={20}
            slidesPerView={1}
            autoplay={{ delay: 4000, disableOnInteraction: false }}
            speed={1500}
            navigation={{
              nextEl: '.custom-next',
              prevEl: '.custom-prev',
            }}
            loop={true}
            breakpoints={{
              500: { slidesPerView: 2 },
              768: { slidesPerView: 3 },
              1024: { slidesPerView: 4 },
            }}
            className="w-full lg:h-110"
          >
            <div>
              {loading
                ? Array.from({ length: 4 }).map((_, idx) => (
                    <SwiperSlide key={idx}>
                      <div className="overflow-hidden rounded-2xl bg-white p-3 shadow-md my-10">
                        <Skeleton height={200} className="rounded-t-xl rounded-b-sm" />
                        <Skeleton height={20} style={{ marginTop: 10 }} count={2} />
                      </div>
                    </SwiperSlide>
                  ))
                : data.filter(Boolean).map((item) => (
                    <SwiperSlide key={item?.id}>
                      <Link to={`/news/${item?.id}`} className="block group">
                        <div className="overflow-hidden rounded-2xl bg-white p-3 shadow-md my-10">
                          <div className="overflow-hidden rounded-t-xl rounded-b-sm h-70">
                            <img
                              src={
                                item?.image_url
                                  ? `${uploadBase}${encodeURI(item?.image_url)}`
                                  : '/default-news.jpg'
                              }
                              alt={item?.text?.[lang] || 'Yangilik rasmi'}
                              className="w-full h-full object-cover group-hover:scale-105 duration-300"
                            />
                          </div>
                          <p className="font-medium text-[17px] text-gray-900 line-clamp-2 group-hover:text-[#E83630] pt-2">
                            {item?.title?.[lang] || 'Sarlavha mavjud emas'}
                          </p>
                        </div>
                      </Link>
                    </SwiperSlide>
                  ))}
            </div>

            {/* Navigatsiya tugmalari (faqat yuklanib bo‘lgach) */}
            {!loading && (
              <>
                <div className="custom-prev absolute left-0 bottom-1/2 w-[40px] h-[40px] rounded-full -translate-y-1/2 z-20 cursor-pointer border text-lg font-bold bg-black/30 backdrop-blur-sm text-[#E83630] hover:bg-[#E83630/90] transition flex items-center justify-center">
                  <FaArrowLeft />
                </div>
                <div className="custom-next absolute right-0 bottom-1/2 w-[40px] h-[40px] rounded-full -translate-y-1/2 z-20 cursor-pointer border text-xl font-bold bg-black/30 backdrop-blur-sm text-[#E83630] p-3 hover:bg-[#E83630/90] transition flex items-center justify-center">
                  <FaArrowRight />
                </div>
              </>
            )}
          </Swiper>
        </div>

        {/* Ko‘proq yangilik tugmasi */}
        <div className="w-full flex justify-center mt-5">
          {loading ? (
            <Skeleton width={220} height={45} borderRadius={999} />
          ) : (
            <NavLink
              to="/news"
              className="mx-auto relative group border-[3px] border-[#E83630] overflow-hidden rounded-full px-10 py-2 flex items-center gap-2"
            >
              <span className="font-one text-[#E83630] group-hover:text-white relative duration-300 z-1">
                {t('news.allNews')}
              </span>
              <FaArrowAltCircleRight className="text-[#E83630] group-hover:text-[#E83630] relative duration-300 z-1" />
              <span className="bg-[#E83630] absolute w-full h-full left-0 top-0 -translate-x-full group-hover:translate-x-0 transition-transform duration-500 z-0" />
            </NavLink>
          )}
        </div>
      </section>
    </div>
  )
}

export default HomeNews
